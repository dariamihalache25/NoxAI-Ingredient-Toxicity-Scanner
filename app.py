def process_image(img):
    """
    Extracts text from an uploaded image using Tesseract OCR,
    cleans the formatting, and splits it into a list of individual ingredients.
    """
    raw_text = pytesseract.image_to_string(img).upper()

    # Isolate the actual ingredient list by discarding marketing text before the headers
    for header in ["INGREDIENTS", "ACTIVE"]:
        if header in raw_text:
            raw_text = raw_text.split(header)[-1]

    if ":" in raw_text:
        text = raw_text.split(":")[-1]
    else:
        text = raw_text

    # Clean formatting and remove non-standard characters generated by OCR artifacts
    text = text.replace('\n', ' ').replace('\r', ' ')
    text = re.sub(r'[^A-Z0-9, ]', '', text)

    elem_list = []
    for i in text.split(','):
        molec = i.strip()

        # Filter out random single-letter OCR errors
        if len(molec) > 2:
            elem_list.append(molec)
    return elem_list

def predict(name):
    """
    Attempts to match an extracted ingredient name against the local database using fuzzy logic.
    If a match is found, passes it to the analysis engine.
    """
    clear_name = re.sub(r'[^A-Z0-9]', '', name.upper())

    # Use fuzzy matching to handle slight spelling errors or OCR misreads (Score out of 100)
    try_match = process.extractOne(clear_name, common_chems, scorer=fuzz.token_set_ratio)

    # Threshold set to 70 to ensure high confidence before assuming a match
    if try_match and try_match[1] > 70:
        return analyze(try_match[0])

    # Check if a substantial part of the chemical name exists within the string
    for chem in common_chems:
        if len(chem) > 5 and chem in clear_name:
            return analyze(chem)
    return "No Structure Found", "blue", name


def analyze(best_match):
    """
    Queries PubChem for chemical structures, generates a molecular fingerprint,
    and predicts toxicity using the trained Random Forest model.
    """

    # Fetch data from PubChem API
    results = pcp.get_compounds(best_match, 'name')

    if not results:
        return "Might not be in the database", "orange", best_match

    # Convert the SMILES string into a 2D molecule object using RDKit
    smiles = results[0].canonical_smiles
    mol = Chem.MolFromSmiles(smiles)
    if mol is None:
        return "Invalid Chemical", "gray", best_match

    # Generate a Morgan Fingerprint (numerical representation of molecular structure)
    generate = AllChem.GetMorganGenerator(radius=2, fpSize=2048)
    fp = generate.GetFingerprint(mol)

    # Pass the fingerprint to the ML model for safety prediction (1 = Toxic, 0 = Safe)
    prediction = model.predict([np.array(fp)])[0]
    if prediction == 1:
        status, color = "Not Safe!", "red"
    else:
        status, color = "Safe :)", "green"
    return status, color, best_match


def log_missing_chemical(original_name, attempted_name="."):
    """
    Logs unmatched chemicals to a Google Sheet (Cloud) or local CSV (Fallback).
    This helps the database administrator track emerging ingredients.
    """
    status = "Confirmed missing" if attempted_name == "." else "Correction failed"
    timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    try:
        # Securely authenticate using Streamlit Cloud Secrets
        credentials_dict = dict(st.secrets["gcp_service_account"])
        gc = gspread.service_account_from_dict(credentials_dict)

        # Target the specific Google Sheet document
        sheet = gc.open("NoxAI Logs").sheet1
        sheet.append_row([timestamp, original_name, attempted_name, status])

    except Exception as e:
        # Fallback to local CSV if running locally without secrets or if API fails
        st.warning(f"Could not connect to Google Sheets. Saving locally.")
        file_exists = os.path.isfile("admin_log.csv")
        with open("admin_log.csv", mode='a', newline='', encoding='utf-8') as f:
            writer = csv.writer(f)
            if not file_exists:
                writer.writerow(["Timestamp", "Original Name", "Attempted Correction", "Status"])
            writer.writerow([timestamp, original_name, attempted_name, status])

