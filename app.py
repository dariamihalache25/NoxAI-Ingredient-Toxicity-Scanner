@st.cache_data
def load_reference():
    """
    Loads and cleans the local chemical reference database.
    Cached by Streamlit to prevent reloading on every user interaction.
    """
    df = pd.read_csv("COSING.csv", on_bad_lines='skip')
    df['clean_name'] = df['INCI name'].astype(str).str.upper().str.replace(r'[^A-Z0-9]', '', regex=True)
    return df

df_reference = load_reference()
common_chems = df_reference['clean_name'].tolist()

